/**
 * Prebuild script — reads cli/templates/** and generates a TypeScript module
 * that exports all template file contents as a Record<string, string>.
 *
 * Usage: node scripts/generate-templates.mjs
 */

import { readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const templatesDir = resolve(__dirname, "../templates");
const outputFile = resolve(__dirname, "../src/site/_generatedTemplates.ts");

/**
 * Recursively collect all files in a directory, returning relative paths.
 */
function collectFiles(dir, base = dir) {
	const entries = [];
	for (const name of readdirSync(dir)) {
		const fullPath = join(dir, name);
		if (statSync(fullPath).isDirectory()) {
			entries.push(...collectFiles(fullPath, base));
		} else {
			entries.push(relative(base, fullPath));
		}
	}
	return entries;
}

const files = collectFiles(templatesDir);
files.sort();

const lines = [
	"// Auto-generated by scripts/generate-templates.mjs — do not edit.",
	"// Run `node scripts/generate-templates.mjs` to regenerate.",
	"// biome-ignore-all lint: auto-generated file",
	"// biome-ignore format: auto-generated file",
	"",
	"export const templates: Record<string, string> = {",
];

for (const relPath of files) {
	const content = readFileSync(join(templatesDir, relPath), "utf-8");
	// Use JSON.stringify to safely escape all content (backticks, ${}, quotes, etc.)
	lines.push(`\t${JSON.stringify(relPath)}: ${JSON.stringify(content)},`);
}

lines.push("};");
lines.push("");

const output = lines.join("\n");

// Only write if content changed (avoid unnecessary rebuilds)
let existing = "";
try {
	existing = readFileSync(outputFile, "utf-8");
} catch {
	// File doesn't exist yet
}

if (existing !== output) {
	writeFileSync(outputFile, output, "utf-8");
	console.log(`Generated ${files.length} templates → ${relative(resolve(__dirname, ".."), outputFile)}`);
} else {
	console.log(`Templates unchanged (${files.length} files)`);
}
